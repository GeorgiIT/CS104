// 2. Сума от цифрите на нечетни позиции на 5 цифрено число
#include <iostream>
using namespace std;
int main()
{
    // Декларираме целочислена променлива
    int a;

    // Въвеждаме стойност от клавиатурата
    cout << "a=?" << endl;
    cin >> a; // 56789

    // Отцепване на цифрите
    int a1 = (a / 10000) % 10; // 5
    int a3 = (a / 100) % 10; // 7
    int a5 = (a / 1) % 10; // 9
    int sum = a5 + a7 + a9;

    // Отпечатваме резултата
    cout << sum << endl;

    return 0;
}