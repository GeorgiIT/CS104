// Сума от цифрите на 5 цифрено число
#include<iostream>
using namespace std;
int main()
{
    // Петцифрено число
    int a;

    // Въвеждане на петцифрено число
    cout << "a=?" << endl;
    cin >> a;

    // Отцепване на нечетните цифри на числото
    int a1 = (a / 10000) % 10;
    int a3 = (a / 100) % 10;
    int a5 = (a / 1) % 10;

    // Сметки
    int s = a1 + a3 + a5;

    // Oтпечатване на сумата на нечетните цифри на числото
    cout << s << endl;

    return 0;
}